    private void txtCedulaFocusLost(java.awt.event.FocusEvent evt)//GEN-FIRST:event_txtCedulaFocusLost
    {//GEN-HEADEREND:event_txtCedulaFocusLost
        m_usuarios modUser = new m_usuarios();
        modUser.setStrCedula(txtCedula.getText());
        
        
        if (objUser.comprobarCedula(modUser))
        {
            txtCedula.setBackground(Color.green);
            btnRegistrar.setEnabled(true);

        } else

        {
            JOptionPane.showMessageDialog(this, "Ingrese una cedula correcta", "Error", JOptionPane.ERROR_MESSAGE);
            txtCedula.selectAll();

            txtCedula.setBackground(Color.red);
            btnRegistrar.setEnabled(false);

        }

    }//GEN-LAST:event_txtCedulaFocusLost

