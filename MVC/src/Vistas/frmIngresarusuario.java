    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt)//GEN-FIRST:event_btnRegistrarActionPerformed
    {//GEN-HEADEREND:event_btnRegistrarActionPerformed
        // TODO add your handling code here:
        boolean booRegistro = false;
        m_usuarios modUser = new m_usuarios();
        if (strTipo == "nuevo")
        {

            modUser.setStrNombre(txtNombre.getText());
            modUser.setStrApellido(txtApellido.getText());
            modUser.setStrCedula(txtCedula.getText());
            modUser.setStrUsuario(txtUsuario.getText());
            modUser.setStrContraceña(txtPassword.getText());
            modUser.setStrContraceña2(txtPassword2.getText());
        }

        if (txtNombre.getText().isEmpty() || txtApellido.getText().isEmpty() || txtCedula.getText().isEmpty() || txtUsuario.getText().isEmpty() || txtPassword.getText().isEmpty())
        {
            JOptionPane.showMessageDialog(this, "Se debe llenar todos los campos", "Error", JOptionPane.ERROR_MESSAGE);

        } else if (txtPassword.getText().equals(txtPassword2.getText()))
        {
            booRegistro = objUser.ingresarUsuario(modUser);

            txtNombre.setText(null);
            txtApellido.setText(null);
            txtCedula.setText(null);
            txtUsuario.setText(null);
            txtPassword.setText(null);
            txtPassword2.setText(null);

        } else
        {
            JOptionPane.showMessageDialog(this, "La contraceña no coincide", "Error", JOptionPane.ERROR_MESSAGE);

            txtPassword.setText(null);
            txtPassword2.setText(null);

        }


    }//GEN-LAST:event_btnRegistrarActionPerformed

